def _exportable_entity_columns(columns: List[str]) -> List[str]:
    """Filter exportable entity columns present in the dataset, excluding ID fields."""
    column_set = set(columns)
    return [
        col
        for col in ENTITY_EXPORT_COLUMNS
        if col in column_set and col not in {"entity_id", "cluster_id"}
    ]


def _format_count(value: Optional[int]) -> str:
    return f"{value:,}" if value is not None else "unknown"


def _parse_bool_env(var_name: str, default: bool = False) -> bool:
    value = os.getenv(var_name)
    if value is None:
        return default
    return value.strip().lower() in {"1", "true", "yes"}


def _normalize_neo4j_batch_size(value: int, source: str) -> int:
    if value <= 0:
        print(
            f"Warning: {source} {value} is invalid; using default {_NEO4J_DEFAULT_BATCH_SIZE}"
        )
        return _NEO4J_DEFAULT_BATCH_SIZE
    return value


def _resolve_neo4j_batch_size(cli_value: Optional[int]) -> int:
    if cli_value is not None:
        return _normalize_neo4j_batch_size(cli_value, "neo4j batch size")
    batch_env = os.getenv("NEO4J_BATCH_SIZE")
    if batch_env:
        try:
            return _normalize_neo4j_batch_size(int(batch_env), "NEO4J_BATCH_SIZE")
        except ValueError:
            print(
                f"Warning: NEO4J_BATCH_SIZE='{batch_env}' is invalid; "
                f"using default {_NEO4J_DEFAULT_BATCH_SIZE}"
            )
            return _NEO4J_DEFAULT_BATCH_SIZE
    return _NEO4J_DEFAULT_BATCH_SIZE


def get_cache_file_path() -> str:
    """Get the path to the cached data file."""
    if CACHE_FORMAT == "parquet":
        return os.path.join(CACHE_DIR, "oracle_data.parquet")
