-- ============================================
-- HELPER FUNCTIONS
-- ============================================

-- Function to compute next audit hash
CREATE OR REPLACE FUNCTION compute_audit_hash(
    p_prev_hash VARCHAR(64),
    p_event_type VARCHAR(50),
    p_payload JSONB,
    p_created_at TIMESTAMP WITH TIME ZONE
) RETURNS VARCHAR(64) AS $$
BEGIN
    RETURN encode(
        sha256(
            (p_prev_hash || p_event_type || p_payload::text || p_created_at::text)::bytea
        ),
        'hex'
    );
END;
$$ LANGUAGE plpgsql IMMUTABLE;

-- ============================================
-- COMMENTS
-- ============================================

COMMENT ON TABLE runs IS 'ER pipeline execution tracking';
COMMENT ON TABLE policy_versions IS 'Versioned policy configurations with maker-checker approval';
COMMENT ON TABLE customers_norm IS 'Normalized customer records from staging';
COMMENT ON TABLE candidate_pairs IS 'Candidate pairs generated by multi-pass blocking';
COMMENT ON TABLE match_scores IS 'Splink probabilistic match scores with evidence';
COMMENT ON TABLE match_decisions IS 'Final decisions: AUTO_LINK, REVIEW, or REJECT';
COMMENT ON TABLE review_queue IS 'Human review queue with maker-checker workflow';
COMMENT ON TABLE clusters IS 'Union-find clustering with versioning';
COMMENT ON TABLE golden_records IS 'Merged golden identity records';
COMMENT ON TABLE audit_events IS 'Append-only tamper-evident audit log';
COMMENT ON TABLE referee_explanations IS 'AI-generated explanations for review assistance';
